const ELF_ROCK: char = 'A';
const ELF_PAPER: char = 'B';
const ELF_SCISSOR: char = 'C';
type MOVE = (char, u32);
const MY_ROCK: MOVE = ('X', 1);
const MY_PAPER: MOVE = ('Y', 2);
const MY_SCISSOR: MOVE = ('Z', 3);
type MatchResult = (char, u32);
const LOSE: MatchResult = ('X', 0);
const DRAW: MatchResult = ('Y', 3);
const WIN: MatchResult = ('Z', 6);

const POSSIBLE_RESULTS: [(char, MOVE, MatchResult); 9] = [
    (ELF_ROCK, MY_ROCK, DRAW),
    (ELF_ROCK, MY_PAPER, WIN),
    (ELF_ROCK, MY_SCISSOR, LOSE),
    (ELF_PAPER, MY_ROCK, LOSE),
    (ELF_PAPER, MY_PAPER, DRAW),
    (ELF_PAPER, MY_SCISSOR, WIN),
    (ELF_SCISSOR, MY_ROCK, WIN),
    (ELF_SCISSOR, MY_PAPER, LOSE),
    (ELF_SCISSOR, MY_SCISSOR, DRAW),
];

fn main() {
    // PART 1
    let total_score: u32 = input
        .split('\n')
        .map(|round| {
            let round_input: Vec<char> = round.chars().collect();
            let elf_move = round_input[0];
            let my_move = round_input[2];

            let match_result = POSSIBLE_RESULTS
                .iter()
                .find(|poss| poss.0 == elf_move && poss.1 .0 == my_move)
                .unwrap();
            let result_score = match_result.2 .1;

            let shape_score = if (my_move == MY_ROCK.0) {
                MY_ROCK.1
            } else if (my_move == MY_PAPER.0) {
                MY_PAPER.1
            } else {
                MY_SCISSOR.1
            };

            return result_score + shape_score;
        })
        .sum();

    println!("{}", total_score);

    // PART 2
    let total_score: u32 = input
        .split('\n')
        .map(|round| {
            let round_input: Vec<char> = round.chars().collect();
            let elf_move = round_input[0];
            let match_result = match round_input[2] {
                'X' => LOSE,
                'Y' => DRAW,
                'Z' => WIN,
                _ => panic!("bad input"),
            };

            let my_move = POSSIBLE_RESULTS
                .iter()
                .find(|poss| poss.0 == elf_move && poss.2 == match_result)
                .unwrap()
                .1;

            let result_score = match_result.1;
            let shape_score = my_move.1;

            return result_score + shape_score;
        })
        .sum();

    println!("{}", total_score);
}
